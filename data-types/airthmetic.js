// substraction -
var sub = 60-30;
console.log(sub);

// Division /
var div =50/4;
console.log(div);

// Multiplication *
var mul = 3*5;
console.log(mul);

// Modulus %
var mod = 55%10;
console.log(mod);

//post Increment ++
var x =10;
x++;
console.log('post increment', x);

// pre increment
var y = 20;
++y;
console.log('pre increment', ++y);
++y;

// Decrement operator
var a = 20;
console.log(a--);

// post decrement
var b=20;
console.log(--b);

// unary plus +

var x = +'3';
var bool = +true;
console.log(x);
console.log(bool);

// unary negation -

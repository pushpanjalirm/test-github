// comparision operator

// Equality ==
var x = 10;
var y = 30;
console.log(x == y);


// not equal !=
console.log(x != y);

// Greater than >
console.log(x > y);

// Greater than equal >=
console.log(x >= y);

// less than <
console.log(x < y);

// less than equal to <=
console.log(x <= y);

// strict equality ===
var a = 10;
var b = '10';

console.log(a === b);

// strict not equal !
// checks for data type also 
console.log(a !== b);